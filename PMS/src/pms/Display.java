        try {                                         
            // TODO add your handling code here:
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                java.util.logging.Logger.getLogger(Insert.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            try {
                conn=DriverManager.getConnection("jdbc:mysql://localhost:3308/pms","root","");
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Insert.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            String data="";
            PreparedStatement stmt=conn.prepareStatement("SELECT * from student");
            ResultSet rs=stmt.executeQuery();
            while(rs.next()){
                String en=rs.getString("eno");
                String fn=rs.getString("fname");
                String ln=rs.getString("lname");
                String se=rs.getString("sem");
                String bn=rs.getString("branch");
                String cgpa=rs.getString("cgpa");
                String com=rs.getString("company");
                
                data +=en+"\t"+fn+"\t"+ln+"\t"+se+"\t"+bn+"\t"+cgpa+"\t"+com+"\n";
                
            }txtarea1.setText(data);
        } catch (SQLException ex) {
            Logger.getLogger(Display.class.getName()).log(Level.SEVERE, null, ex);
        }
